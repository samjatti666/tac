// Initialize Particles.js
document.addEventListener('DOMContentLoaded', function() {
    // Particles.js Configuration
    particlesJS('particles-js', {
        particles: {
            number: {
                value: 80,
                density: {
                    enable: true,
                    value_area: 800
                }
            },
            color: {
                value: '#FFD700'
            },
            shape: {
                type: 'circle'
            },
            opacity: {
                value: 0.5,
                random: true,
                animation: {
                    enable: true,
                    speed: 1,
                    minimumValue: 0.1,
                    sync: false
                }
            },
            size: {
                value: 3,
                random: true,
                animation: {
                    enable: true,
                    speed: 2,
                    minimumValue: 0.1,
                    sync: false
                }
            },
            line_linked: {
                enable: true,
                distance: 150,
                color: '#FFD700',
                opacity: 0.2,
                width: 1
            },
            move: {
                enable: true,
                speed: 1,
                direction: 'none',
                random: true,
                straight: false,
                outModes: 'out',
                attract: {
                    enable: true,
                    rotateX: 600,
                    rotateY: 1200
                }
            }
        },
        interactivity: {
            detectsOn: 'canvas',
            events: {
                onHover: {
                    enable: true,
                    mode: 'grab'
                },
                onClick: {
                    enable: true,
                    mode: 'push'
                },
                resize: true
            },
            modes: {
                grab: {
                    distance: 140,
                    lineLinked: {
                        opacity: 0.5
                    }
                },
                push: {
                    particles_nb: 4
                }
            }
        },
        retina_detect: true
    });

    // Load Lottie Animation
    const container = document.getElementById('hero-animation');
    if (container) {
        const lottiePlayer = document.createElement('lottie-player');
        lottiePlayer.src = 'https://assets3.lottiefiles.com/packages/lf20_x8qaqhnw.json'; // Creative tech animation
        lottiePlayer.background = 'transparent';
        lottiePlayer.speed = '1';
        lottiePlayer.style.width = '100%';
        lottiePlayer.style.height = '100%';
        lottiePlayer.loop = true;
        lottiePlayer.autoplay = true;
        container.appendChild(lottiePlayer);
    }

    // Magnetic Button Effect
    const buttons = document.querySelectorAll('.magnetic-button');
    buttons.forEach(button => {
        button.addEventListener('mousemove', (e) => {
            const rect = button.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            
            const deltaX = (x - centerX) / centerX;
            const deltaY = (y - centerY) / centerY;
            
            button.style.transform = `translate(${deltaX * 10}px, ${deltaY * 10}px) scale(1.05)`;
        });

        button.addEventListener('mouseleave', () => {
            button.style.transform = 'translate(0, 0) scale(1)';
        });
    });
});
